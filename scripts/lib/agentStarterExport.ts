async function getManagedFiles(repoRoot: string): Promise<ManagedFile[]> {
  const skillsSourcePath = path.join(repoRoot, SOURCE_SKILLS_PATH);
  const clientPackagePath = path.join(repoRoot, CLIENT_PACKAGE_PATH);
  const rootPackagePath = path.join(repoRoot, ROOT_PACKAGE_PATH);
  const skillsSource = await readFile(skillsSourcePath, "utf8");
  const clientPackage = await readJsonFile<{ devDependencies?: Record<string, string> }>(clientPackagePath);
  const rootPackage = await readJsonFile<{ packageManager?: string }>(rootPackagePath);
  const playwrightVersion = clientPackage.devDependencies?.playwright ?? "^1.58.2";
  const packageManager = rootPackage.packageManager;
  const pnpmVersion = parsePnpmVersion(packageManager);

  return [
    { path: "skills.md", content: renderSkillsMirror(skillsSource) },
    { path: ".gitignore", content: renderGitIgnore() },
    { path: "package.json", content: renderPackageJson(playwrightVersion, packageManager) },
    { path: "src/contract.mjs", content: renderContractModule() },
    { path: "src/index.mjs", content: renderIndexModule() },
    { path: "examples/no-context-smoke.mjs", content: renderNoContextSmokeExample() },
    { path: "examples/baseline-loop.mjs", content: renderBaselineLoopExample() },
    { path: ".github/workflows/public-agent-smoke.yml", content: renderGitHubWorkflow(pnpmVersion) },
  ];
}

async function readExistingManagedFiles(targetRoot: string): Promise<string[]> {
  const manifestPath = path.join(targetRoot, EXPORT_MANIFEST_PATH);
  if (!(await pathExists(manifestPath))) {
    return [];
  }

  const manifest = await readJsonFile<ExistingManifest>(manifestPath);
  return Array.isArray(manifest.managedFiles)
    ? manifest.managedFiles
      .map((entry) => (typeof entry.path === "string" ? entry.path : null))
      .filter((entry): entry is string => entry !== null)
    : [];
}

async function readOriginUrl(targetRoot: string): Promise<string | null> {
  try {
    const { stdout } = await execFile("git", ["-C", targetRoot, "config", "--get", "remote.origin.url"]);
    const normalized = stdout.trim();
    return normalized.length > 0 ? normalized : null;
  } catch {
    return null;
  }
}

function assertSafeTarget(repoRoot: string, targetRoot: string): void {
  if (targetRoot === repoRoot) {
    throw new Error("Refusing to export into the game repo itself.");
  }
  if (isPathInside(repoRoot, targetRoot)) {
    throw new Error("Refusing to export into a directory nested inside the game repo.");
  }
}

async function validateTarget(targetRoot: string, expectOrigin?: string): Promise<void> {
  const targetExists = await pathExists(targetRoot);
  if (!targetExists) {
    if (expectOrigin) {
      throw new Error("Cannot validate --expect-origin before the starter repo exists.");
    }
    return;
  }

  const targetStat = await stat(targetRoot);
  if (!targetStat.isDirectory()) {
    throw new Error(`Export target is not a directory: ${targetRoot}`);
  }

  const gitDirPath = path.join(targetRoot, ".git");
  if (!(await pathExists(gitDirPath))) {
    throw new Error("Existing export target must be a separate git repository with a .git directory.");
  }

  if (!expectOrigin) {
    return;
  }

  const originUrl = await readOriginUrl(targetRoot);
  if (!originUrl) {
    throw new Error("Starter repo has no origin remote configured.");
  }
  if (normalizeRemoteUrl(originUrl) !== normalizeRemoteUrl(expectOrigin)) {
    throw new Error(`Starter repo origin mismatch: expected ${expectOrigin}, got ${originUrl}`);
  }
}

async function writeManagedFiles(targetRoot: string, files: ManagedFile[]): Promise<void> {
  for (const file of files) {
    const outputPath = path.join(targetRoot, file.path);
    await mkdir(path.dirname(outputPath), { recursive: true });
    await writeFile(outputPath, file.content);
  }
}

async function ensureTargetRepo(targetRoot: string): Promise<void> {
  if (await pathExists(targetRoot)) {
    return;
  }
  await mkdir(targetRoot, { recursive: true });
  await execFile("git", ["init", "--quiet", targetRoot]);
}

async function removeStaleManagedFiles(targetRoot: string, previousManagedFiles: string[], nextManagedFiles: string[]): Promise<void> {
  const nextManagedFileSet = new Set(nextManagedFiles);
  for (const relativePath of previousManagedFiles) {
    if (nextManagedFileSet.has(relativePath)) {
      continue;
    }
    await rm(path.join(targetRoot, relativePath), { force: true });
  }
}

export async function exportAgentStarterRepo(options: ExportAgentStarterOptions = {}): Promise<ExportAgentStarterResult> {
  const repoRoot = path.resolve(options.repoRoot ?? DEFAULT_REPO_ROOT);
  const targetRoot = path.resolve(options.outDir ?? defaultTargetRoot(repoRoot));
  const dryRun = options.dryRun === true;

  assertSafeTarget(repoRoot, targetRoot);
  await validateTarget(targetRoot, options.expectOrigin);

  const files = await getManagedFiles(repoRoot);
  const managedFiles = files
    .map((file) => ({
      path: file.path,
      sha256: sha256(file.content),
    }))
    .sort((left, right) => left.path.localeCompare(right.path));

  const manifest = `${JSON.stringify({
    exportVersion: 1,
    contract: {
      apiVersion: PUBLIC_AGENT_API_VERSION,
      contract: PUBLIC_AGENT_CONTRACT,
      canonicalHost: PUBLIC_AGENT_CANONICAL_HOST,
      canonicalSkillsUrl: PUBLIC_AGENT_CANONICAL_SKILLS_URL,
      companionRepoName: PUBLIC_AGENT_COMPANION_REPO_NAME,
      companionRepoUrl: PUBLIC_AGENT_COMPANION_REPO_URL,
    },
    manifestPath: EXPORT_MANIFEST_PATH,
    managedFiles,
  }, null, 2)}\n`;

  const nextFiles = [
    ...files,
    { path: EXPORT_MANIFEST_PATH, content: manifest },
  ];

  if (!dryRun) {
    await ensureTargetRepo(targetRoot);
    const previousManagedFiles = await readExistingManagedFiles(targetRoot);
    await writeManagedFiles(targetRoot, nextFiles);
    await removeStaleManagedFiles(targetRoot, previousManagedFiles, nextFiles.map((file) => file.path));
  }

  return {
    targetRoot,
    dryRun,
    manifestPath: EXPORT_MANIFEST_PATH,
    managedFiles,
  };
}
