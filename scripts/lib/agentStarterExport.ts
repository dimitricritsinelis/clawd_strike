function defaultTargetRoot(repoRoot: string): string {
  return path.resolve(repoRoot, "..", DEFAULT_TARGET_DIRNAME);
}

function normalizeRemoteUrl(value: string): string {
  return value.trim().replace(/\.git$/, "");
}

function isPathInside(parentPath: string, childPath: string): boolean {
  const relativePath = path.relative(parentPath, childPath);
  return relativePath !== "" && !relativePath.startsWith("..") && !path.isAbsolute(relativePath);
}

async function pathExists(filePath: string): Promise<boolean> {
  try {
    await access(filePath);
    return true;
  } catch {
    return false;
  }
}

async function readJsonFile<T>(filePath: string): Promise<T> {
  return JSON.parse(await readFile(filePath, "utf8")) as T;
}

function sha256(content: string): string {
  return createHash("sha256").update(content).digest("hex");
}

function parsePnpmVersion(packageManager: string | undefined): string {
  if (!packageManager?.startsWith("pnpm@")) {
    return "10";
  }
  return packageManager.slice("pnpm@".length);
}

